public class ButtonLoadGame extends ScaledTouchUpButton {
    public ButtonLoadGame(TextureAtlas atlas, ActionListener actionListener) {
        super(atlas.findRegion("btPlay"), actionListener);
    }

    @Override
    public void resize(Rect worldBounds) {
        setBottom(worldBounds.getBottom());
        setLeft(-0.07f);
    }
}
